def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Create a manual-seed YouTube production run package."
    )
    parser.add_argument("--topic", required=True, help="Topic or idea for the run.")
    parser.add_argument("--category", default="", help="YouTube category or niche.")
    parser.add_argument("--format", default="shorts", help="shorts, long-form, explainer, etc.")
    parser.add_argument("--language", default="ko", help="Primary script language.")
    parser.add_argument("--target-audience", default="", help="Target viewer segment.")
    parser.add_argument("--tone", default="", help="Channel tone or style.")
    parser.add_argument(
        "--duration-seconds",
        type=int,
        default=60,
        help="Target duration in seconds.",
    )
    parser.add_argument(
        "--seed-url",
        action="append",
        default=[],
        help="Manual YouTube source URL. Repeat for multiple seeds.",
    )
    parser.add_argument("--run-id", default="", help="Optional explicit run id.")
    parser.add_argument(
        "--output-dir",
        default=str(ROOT / "runs"),
        help="Directory where run folders are created.",
    )
    parser.add_argument("--force", action="store_true", help="Overwrite an existing run folder.")
    args = parser.parse_args(argv)

    if args.duration_seconds < 1:
        parser.error("--duration-seconds must be greater than 0")
    if not args.seed_url:
        parser.error("at least one --seed-url is required for manual-seed Phase 1")
    return args


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    timestamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    run_id = args.run_id or f"{timestamp}-{slugify(args.topic)}"
    output_dir = Path(args.output_dir).resolve()
    run_dir = output_dir / run_id

    if run_dir.exists() and not args.force:
        print(f"Run directory already exists: {run_dir}", file=sys.stderr)
        print("Use --force to overwrite.", file=sys.stderr)
        return 1

    run_dir.mkdir(parents=True, exist_ok=True)

    sources = [build_source(url, index) for index, url in enumerate(args.seed_url, start=1)]
    package = build_package(args, run_id, sources)
    created_at = utc_now_iso()

    manifest = {
        "run_id": run_id,
        "created_at": created_at,
        "source_mode": "manual_seed",
        "status": "needs_review",
        "paths": {
            "production_package": "production-package.json",
            "brief": "brief.json",
            "sources": "sources.json",
            "qa": "08-qa.md",
        },
        "next_actions": [
            "Enrich research with youtube-market-research.",
            "Analyze source videos with youtube-video-analysis.",
            "Build claim ledger with youtube-fact-check.",
            "Draft script plan with youtube-script-architect.",
        ],
    }

    write_json(run_dir / "manifest.json", manifest)
    write_json(run_dir / "brief.json", package["brief"])
    write_json(run_dir / "sources.json", sources)
    write_json(run_dir / "production-package.json", package)
    write_markdown_files(run_dir, package)

    print(f"Created run: {run_id}")
    print(f"Run directory: {run_dir}")
    print(f"Source videos: {len(sources)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))

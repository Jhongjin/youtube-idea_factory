#!/usr/bin/env python3
"""Create a manual-seed YouTube production run package."""

from __future__ import annotations

import argparse
import json
import re
import sys
from datetime import datetime, timezone
from pathlib import Path
from urllib.parse import parse_qs, urlparse


ROOT = Path(__file__).resolve().parents[1]


def slugify(value: str) -> str:
    value = value.strip().lower()
    value = re.sub(r"[^a-z0-9]+", "-", value)
    value = value.strip("-")
    return value[:48] or "run"


def extract_youtube_video_id(url: str) -> str:
    parsed = urlparse(url)
    host = parsed.netloc.lower().replace("www.", "")

    if host == "youtu.be":
        return parsed.path.strip("/").split("/")[0]

    if host.endswith("youtube.com"):
        query_video_id = parse_qs(parsed.query).get("v")
        if query_video_id:
            return query_video_id[0]

        parts = [part for part in parsed.path.split("/") if part]
        for marker in ("shorts", "embed", "live"):
            if marker in parts:
                index = parts.index(marker)
                if len(parts) > index + 1:
                    return parts[index + 1]

    return ""


def utc_now_iso() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def build_source(seed_url: str, index: int) -> dict:
    video_id = extract_youtube_video_id(seed_url)
    title_suffix = video_id if video_id else seed_url
    return {
        "rank": index,
        "url": seed_url,
        "title": f"Manual seed {index}: {title_suffix}",
        "channel": "",
        "inclusion_reason": "Manual seed URL provided during intake.",
        "transcript_status": "not_checked",
        "video_id": video_id,
        "source_mode": "manual_seed",
    }


def build_package(args: argparse.Namespace, run_id: str, sources: list[dict]) -> dict:
    return {
        "run_id": run_id,
        "brief": {
            "topic": args.topic,
            "category": args.category or "",
            "format": args.format,
            "target_audience": args.target_audience or "",
            "target_duration_seconds": args.duration_seconds,
            "language": args.language,
            "tone": args.tone or "",
        },
        "sources": sources,
        "claim_ledger": [],
        "script_plan": {
            "angle": "Needs strategy selection after research and analysis.",
            "hook": "Needs hook drafting after competitor analysis.",
            "outline": [
                "Research intake created.",
                "Analyze seed videos.",
                "Fact-check claims.",
                "Draft source-backed script plan.",
            ],
            "notes": "Generated by manual-seed Phase 1 bootstrap. Fill with youtube-script-architect after research and fact-checking.",
        },
        "storyboard": [],
        "media_prompts": {
            "style_bible": "",
            "image_prompts": [],
            "video_prompts": [],
        },
        "publishing_package": {
            "title_candidates": [],
            "description": "",
            "tags": [],
            "thumbnail_prompt": "",
        },
        "qa": {
            "status": "needs_review",
            "blockers": [
                "Competitor video analysis is not complete.",
                "Claim ledger is empty.",
                "Script plan is only a bootstrap placeholder.",
                "Storyboard and media prompts are not complete.",
                "Human approval is required before generation or publishing.",
            ],
            "approval_required": True,
        },
    }


def markdown_table(headers: list[str], rows: list[list[str]]) -> str:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        escaped = [cell.replace("\n", " ").replace("|", "\\|") for cell in row]
        lines.append("| " + " | ".join(escaped) + " |")
    return "\n".join(lines)


def write_json(path: Path, payload: object) -> None:
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


